class SwappingStrings
{
 public static void main(String[] args)
 {
 String st1="Suresh";
 String st2="Adabala";
 System.out.println("Before Swapping st1: "+st1);
 System.out.println("Before Swapping st2: "+st2);
 String st3=st1;
 st1=st2;
 st2=st3;
 System.out.println("After Swapping st1: "+st1);
 System.out.println("After Swapping st2: "+st2);
 }
}

